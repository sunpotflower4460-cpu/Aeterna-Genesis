ROOM_ID = "room-g003-a"
N_STAR = 128
STEPS = 1500
SEEDS = [0, 1, 2]
CONV_N = [96, 128, 160]
DT = mh.DEFAULTS["dt"]


def _simulate(N, seed, steps, coupling=1.0):
    """Evolve Model H from uniform + noise; return measured trajectory + a final-field checksum."""
    s = mh.ModelH(N, seed=seed, coupling=coupling)
    F, mass, amp, scale, ke, interf = [], [], [], [], [], []
    rec = lambda: (F.append(s.free_energy()), mass.append(s.mass()), amp.append(s.phase_amplitude()),
                   scale.append(s.structure_scale()), ke.append(s.kinetic_energy()),
                   interf.append(s.interface_fraction()))
    rec()
    snap = max(1, steps // 12)
    for t in range(1, steps + 1):
        s.step(DT)
        if not s.finite():
            raise RuntimeError("non-finite Model H at N=%d seed=%d step=%d" % (N, seed, t))
        if t % snap == 0 or t == steps:
            rec()
    h = hashlib.sha256()
    for arr in s.state_hash_arrays():
        h.update(np.ascontiguousarray(arr.real).tobytes())
        h.update(np.ascontiguousarray(arr.imag).tobytes())
    mass_drift = float(max(abs(m - mass[0]) for m in mass))
    f_monotone = all(F[i] >= F[i + 1] - 1e-6 for i in range(len(F) - 1))
    return {"N": N, "seed": seed, "steps": steps, "coupling": coupling,
            "amp0": amp[0], "amp": amp[-1], "scale0": scale[0], "scale": scale[-1],
            "F0": F[0], "F": F[-1], "ke": ke[-1], "interface": interf[-1],
            "mass_drift": mass_drift, "f_monotone": bool(f_monotone),
            "separated": bool(amp[-1] > 0.5 and scale[-1] > 2 * scale[0]),
            "localized": bool(interf[-1] < 0.6), "checksum": h.hexdigest()}


def _emergence_doc(r, contrast, reached):
    co_evolution = bool(r["ke"] > 1e-4 and r["scale"] > contrast["scale"])   # flow generated + faster coarsening
    return {
        "reached_level": reached, "candidate_level": 5,
        "uninterrupted_from_zero": True, "level_detected_by_measurement": True,
        "detected": {"difference": bool(r["separated"]), "localization": bool(r["localized"]),
                     "spontaneous_motion": bool(r["ke"] > 1e-4), "circulation": bool(r["ke"] > 1e-4),
                     "co_differentiation": co_evolution},
        "measured_by": {
            "phase_amplitude_start": round(r["amp0"], 4), "phase_amplitude_final": round(r["amp"], 4),
            "domain_scale_start": round(r["scale0"], 3), "domain_scale_final": round(r["scale"], 3),
            "interface_fraction_final": round(r["interface"], 4),
            "free_energy_drop": round(r["F0"] - r["F"], 5), "kinetic_energy_final": round(r["ke"], 6),
            "mass_drift": r["mass_drift"],
            "coarsening_gain_vs_noflow": round(r["scale"] / contrast["scale"], 4),
            "flow_generated_from_field": bool(r["ke"] > 1e-4),
            "coupling_contrast_holds": co_evolution,
        },
        "purity": {"per_object_labels": False, "external_optimum": False,
                   "role": "E" if reached >= 1 else "F"},
        "natural_emergence": {"started_from_time_zero": True, "target_shape_seeded": False,
                              "runtime_interventions": 0, "target_dependent_rules": False,
                              "target_dependent_stopping": False, "target_dependent_clipping": False,
                              "level_detected_by_measurement": True},
    }


def _genesis(N, seed):
    return {
        "schema_version": 1, "model": mh.MODEL_ID, "dimension": 2,
        "domain": {"size": [N, N], "spacing": round(mh.DEFAULTS["L"] / N, 6), "boundary": "periodic"},
        "fields": {"phi": {"type": "scalar"}, "velocity": {"type": "vector"}},
        "initial_state": {"type": "uniform_plus_noise", "mean_amplitude": mh.DEFAULTS["mean_phi"],
                          "noise_amplitude": mh.DEFAULTS["noise_amplitude"], "correlation_length": 1.0},
        "laws": ["cahn_hilliard", "navier_stokes", "model_h", "incompressible"],
        "seed": seed, "drive_class": "autonomous",
    }


def _write_run(out, r, reached, emergence):
    d = os.path.join(out, "runs", "seed-%04d" % r["seed"])
    os.makedirs(d, exist_ok=True)
    manifest = {
        "run_id": "%s-2d-seed%04d" % (mh.MODEL_ID, r["seed"]), "room_id": ROOM_ID,
        "seed": r["seed"], "dimension": 2, "mode": "2d-screen",
        "manifest": {"genesis": "g003", "solver": mh.MODEL_ID, "steps": r["steps"], "dt": DT,
                     "grid": [r["N"], r["N"]], "code_version": runner.CODE_VERSION},
        "summary": {"reached_level": reached, "conservation_drift": round(r["mass_drift"], 12)},
        "checksum": {"final_field_sha256": r["checksum"]},
    }
    summary = {"reached_level": reached, "conservation_drift": round(r["mass_drift"], 12),
               "phase_amplitude_final": round(r["amp"], 5), "domain_scale_final": round(r["scale"], 4),
               "kinetic_energy_final": round(r["ke"], 6), "free_energy_drop": round(r["F0"] - r["F"], 5)}
    json.dump(manifest, open(os.path.join(d, "manifest.json"), "w"), indent=2)
    json.dump(summary, open(os.path.join(d, "summary.json"), "w"), indent=2)
    json.dump({"final_field_sha256": r["checksum"]}, open(os.path.join(d, "checksum.json"), "w"), indent=2)
    json.dump(emergence, open(os.path.join(d, "emergence.json"), "w"), indent=2)


def build(out_root, quick=False):
    if quick:
        nstar, seeds, conv, steps = 64, [0, 1], [48, 64], 900
    else:
        nstar, seeds, conv, steps = N_STAR, SEEDS, CONV_N, STEPS
    out = os.path.join(out_root, "rooms", "official", ROOM_ID)
    os.makedirs(out, exist_ok=True)

    # official multi-seed coupled (Model H) runs
    runs = [_simulate(nstar, s, steps, coupling=1.0) for s in seeds]
    # coupling contrast (C=0, pure Cahn-Hilliard): no flow -> shows the flow is generated BY the field
    contrast = _simulate(nstar, seeds[0], steps, coupling=0.0)
    reached = 2 if all(r["separated"] and r["localized"] for r in runs) else (
        1 if all(r["separated"] for r in runs) else 0)
    emergences = [_emergence_doc(r, contrast, reached) for r in runs]

    # reproducibility
    repro = _simulate(nstar, seeds[0], steps, coupling=1.0)["checksum"] == runs[0]["checksum"]
    # conservation: exact mass + monotone free-energy decrease (H-theorem)
    mass_ok = all(r["mass_drift"] < 1e-8 for r in runs)
    f_ok = all(r["f_monotone"] for r in runs)
    conservation = mass_ok and f_ok
    # convergence: saturated phase contrast grid-independent, separation on every grid
    conv_rows = []
    for N in conv:
        rc = runs[0] if N == nstar else _simulate(N, seeds[0], steps, coupling=1.0)
        conv_rows.append({"N": N, "phase_amplitude": round(rc["amp"], 4),
                          "domain_scale": round(rc["scale"], 3), "separated": rc["separated"]})
    amps = [row["phase_amplitude"] for row in conv_rows]
    amp_spread = (max(amps) - min(amps)) / np.mean(amps)
    convergence = amp_spread < 0.10 and all(row["separated"] for row in conv_rows)
    # co-evolution audit (the Model H signature) drives integrity
    co_evolution = emergences[0]["measured_by"]["coupling_contrast_holds"]

    for r, em in zip(runs, emergences):
        _write_run(out, r, reached, em)

    docs = _assemble_docs(nstar, seeds, runs, contrast, reached, repro, conservation, convergence)
    for name, doc in docs["yaml"].items():
        with open(os.path.join(out, name), "w") as fh:
            yaml.safe_dump(doc, fh, allow_unicode=True, sort_keys=False, width=100)
    convdoc = {"resolutions": conv, "rows": conv_rows, "amp_spread_rel": round(float(amp_spread), 4),
               "level_converged": bool(convergence), "reproducible_checksum": bool(repro),
               "mass_conserved": bool(mass_ok), "free_energy_monotone_decreasing": bool(f_ok),
               "coupling_contrast": {"ke_coupled": round(runs[0]["ke"], 6), "ke_noflow": round(contrast["ke"], 8),
                                     "scale_coupled": round(runs[0]["scale"], 3),
                                     "scale_noflow": round(contrast["scale"], 3),
                                     "hydrodynamic_coarsening": co_evolution}}
    json.dump(convdoc, open(os.path.join(out, "convergence.json"), "w"), indent=2)
    json.dump(emergences[0], open(os.path.join(out, "emergence.json"), "w"), indent=2)
    with open(os.path.join(out, "README.md"), "w") as fh:
        fh.write(docs["readme"])

    _validate(out, len(seeds))
    return {"out": out, "reached": reached, "conv_rows": conv_rows, "repro": repro,
            "conservation": conservation, "convergence": convergence, "co_evolution": co_evolution,
            "contrast": contrast, "runs": runs}


